    public boolean onTouchEvent(MotionEvent touchEvent) {
        switch (touchEvent.getAction()) {
            case MotionEvent.ACTION_DOWN:
                x1 = touchEvent.getX();
                y1 = touchEvent.getY();
                break;
            case MotionEvent.ACTION_UP:
                x2 = touchEvent.getX();
                y2 = touchEvent.getY();
                if (y1 > y2) { // swipe up
                    Intent i = new Intent(MainActivity.this, SwipeUp.class);
                    startActivity(i);
                } else if (y1 < y2) { // swipe down
                    Intent i = new Intent(MainActivity.this, SwipeDown.class);
                    startActivity(i);
                }
                break;
        }
        return false;
    }

